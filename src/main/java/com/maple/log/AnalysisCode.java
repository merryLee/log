public class AnalysisCode {

	public String name;
	public int count;
	public double percent;

	public AnalysisCode() {
	}

	public AnalysisCode(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}
	
	public int getCount() {
		return count;
	}

}
